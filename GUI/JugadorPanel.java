    private Jugador jugador;
    
    public void setJugador (Jugador jugador){
        this.jugador = jugador;        
        
        // Imprimimos la informaci칩n del jugador con los tres campos de texto
        textFieldNombre.setText(this.jugador.getNombre());
        textFieldSaldo.setText(Float.toString(this.jugador.getSaldo()));
        textFieldEspeculador.setText(Boolean.toString(this.jugador.getEsJugadorEspeculador()));
        
        // Imprimimos sus propiedades
        this.rellenaPropiedades(this.jugador.getPropiedades());
        
        repaint();
        revalidate();
    }
    
    public void rellenaPropiedades(ArrayList<CasillaCalle> lista){
        // Se elimina la informaci칩n antigua
        propiedadesPanel.removeAll();
        
        // Se recorre la lista de propiedades para ir creando sus vistas individuales y a침adirlas al panel
        for (CasillaCalle t : lista) {
            PropiedadPanel vistaPropiedad = new PropiedadPanel();
            vistaPropiedad.setPropiedad(t);

            propiedadesPanel.add(vistaPropiedad);
            vistaPropiedad.setVisible(true);
        }
        // Se fuerza la actualizaci칩n visual del panel propiedades y del panel del jugador
        repaint();
        revalidate();
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
