    private Casilla casilla;
    
    public void setCasillaTablero (Casilla casilla, ArrayList<Jugador> jugadores, ArrayList<Casilla> tablero){
        this.casilla = casilla;
        
        // Nombre de la casilla:
        textFieldNombre.setText(this.casilla.getNombre());
        
        /////////
        // Para pintar las "fichas" del tablero para cada jugador, hacemos lo siguiente:
        
        labelRed.setVisible(false); // De primeras hacemos que ninguna ficha sea visible
        labelBlue.setVisible(false);
        labelYellow.setVisible(false);
        labelGreen.setVisible(false);
        
        // Con este bucle for comprobamos si algun jugador esta en la casilla actual y actualizamos conforme a ello
        for (Jugador jug:jugadores){
            int casillaJug = jug.getCasillaActual(); // La casilla en la que se encuentra jug
            int indiceCasilla = tablero.indexOf(casilla); // El indice en el tablero de la casilla que ocupa este panel
            
            // Si la casilla en la que est√° el jugador es igual a la actual...
            if (casillaJug == indiceCasilla){
                int indiceJug = jugadores.indexOf(jug); // Indice del jugador en el array
                
                switch (indiceJug){
                    case 0:
                        labelRed.setVisible(true);  // Si es el jugador 1, se muestra el punto rojo
                        break;
                    case 1:
                        labelBlue.setVisible(true);    // Si es el jugador 2, se muestra el azul
                        break;
                    case 2:
                        labelGreen.setVisible(true);   // ...
                        break;
                    case 3:
                        labelYellow.setVisible(true);  // ...
                        break;
                }
            }
            repaint();
            revalidate();
        }
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
