    private CasillaCalle calle;
    
    public void setCalle (CasillaCalle calle){
        this.calle = calle;
        
        String propietario;
        if (calle.tienePropietario())
            propietario = calle.getPropietario().getNombre();
        else
            propietario = "no tiene";
        
        textFieldPropietario.setText(propietario);
        textFieldCompra.setText(Float.toString(calle.getPrecioCompra()));
        textFieldEdificar.setText(Float.toString(calle.getPrecioEdificar()));
        //
        textFieldAlquiler.setText(Float.toString(calle.getPrecioAlquilerCompleto()));
        
        repaint();
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
